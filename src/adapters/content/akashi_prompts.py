def system_prompt(task: ContentTask) -> str:
    if task is ContentTask.INSTAGRAM_TO_LINKEDIN:
        # This workflow must preserve the source facts. The broad brand context
        # encourages expansion with generic business implications, which is the
        # opposite of a concise platform adaptation.
        return TASK_PROMPTS[task]
    return f"{BRAND_CONTEXT}\n\n{TASK_PROMPTS[task]}"
